/**
 * Knight class implements warrior and has attack and defend methods
 * @author Adam Nguyen
 *
 */
public class Knight implements Warrior{
	/**
	 * attack method prints out an attack
	 */
	public void attack()
	{
		System.out.println("Attacking: I'm bringing you down!");
	}
	/**
	 * defend method prints out defense
	 */
	public void defend()
	{
		System.out.println("Defending: We must protect the castle");
	}

}
